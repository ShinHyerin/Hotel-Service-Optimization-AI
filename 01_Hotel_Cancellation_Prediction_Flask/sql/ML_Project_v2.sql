-- 1. 기존 테이블 삭제
DROP TABLE BOOKINGS;

-- 2. 현재 피처 세팅에 맞춘 테이블 재생성
CREATE TABLE BOOKINGS (
    HOTEL                         VARCHAR2(50),
    LEAD_TIME                     NUMBER,
    COUNTRY                       VARCHAR2(50),
    MARKET_SEGMENT                VARCHAR2(50),
    PREVIOUS_CANCELLATIONS        NUMBER,
    CUSTOMER_TYPE                 VARCHAR2(50),
    REQUIRED_CAR_PARKING_SPACES   NUMBER,
    TOTAL_OF_SPECIAL_REQUESTS     NUMBER,
    IS_CANCELED                   NUMBER
);

-- 확인용 조회 (처음엔 비어있어야 정상)
SELECT * FROM BOOKINGS;

SELECT * FROM dual;

SELECT COUNT(*) FROM BOOKINGS;

SELECT * FROM BOOKINGS
WHERE ROWNUM <= 20;




CREATE TABLE USER_BOOKINGS (

    ID NUMBER PRIMARY KEY,

    CUSTOMER_NAME VARCHAR2(100),

    BIRTH_YEAR NUMBER,
    BIRTH_MONTH NUMBER,
    BIRTH_DAY NUMBER,

    HOTEL_TYPE VARCHAR2(50),

    COUNTRY VARCHAR2(50),

    PHONE VARCHAR2(30),

    LEAD_TIME NUMBER,

    ADR VARCHAR2(30),

    PREVIOUS_CANCELLATIONS NUMBER,

    MARKET_SEGMENT VARCHAR2(100),

    CUSTOMER_TYPE VARCHAR2(100),

    REQUIRED_CAR_PARKING_SPACES NUMBER,

    TOTAL_OF_SPECIAL_REQUESTS NUMBER,

    CREATED_AT DATE DEFAULT SYSDATE
);


CREATE SEQUENCE USER_BOOKINGS_SEQ
START WITH 1
INCREMENT BY 1
NOCACHE;


CREATE OR REPLACE TRIGGER USER_BOOKINGS_TRIGGER
BEFORE INSERT ON USER_BOOKINGS
FOR EACH ROW
BEGIN
    :NEW.ID := USER_BOOKINGS_SEQ.NEXTVAL;
END;
/


